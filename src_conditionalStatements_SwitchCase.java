package conditionalStatements;

public class SwitchCase {

	public static void main(String[] args) {
	
	int	dayofweek = 4;
	
	switch(dayofweek) {
	
	case 1:
		System.out.println("im on leave");
		break;
	case 2:
		System.out.println("im in office");
		break;
	case 3:
		System.out.println("im playing football");
		break;
		default:
			System.out.println("i dont know what day it is.");
			
	}
		

	}

}
